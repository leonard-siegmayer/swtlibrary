package de.teamA.SWT.repository;

public class MediumRepositoryWrapperException extends Exception {

    public MediumRepositoryWrapperException() {
        super();
    }

    public MediumRepositoryWrapperException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public MediumRepositoryWrapperException(String message, Throwable cause) {
        super(message, cause);
    }

    public MediumRepositoryWrapperException(String message) {
        super(message);
    }

    public MediumRepositoryWrapperException(Throwable cause) {
        super(cause);
    }

}
